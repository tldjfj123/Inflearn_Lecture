# include <iostream>
using namespace std;

int main() {
	// char : 작은 문자형, 한가지 문자만 저장, 작은 따옴표로 정의('')
	char b = 'a';
	// null 문자 >> '\0'
	// "" >> 명시적으로 null문자가 포함. >> string
	cout << b << endl;

	// bool : 0 or 1 

	bool x = 0;
	bool y = 1;
	bool z = 10;

	cout << x << y << z << endl;

	return 0;
}